    private void ModActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModActionPerformed
        // Retrieve the values from the UI components
        String selectedDate_Debut = formatDate(Date_Debut.getDate());
        String selectedDate_Fin = formatDate(Date_Fin.getDate());
        String selectedDuree = Duree.getText();
        String selectedPrix = Prix.getText();

        // Update the reservation using the selected ID
        action.modifierReservation(selectedDate_Debut, selectedDate_Fin, selectedDuree, selectedPrix, selectedid);

        // Show a success message
        JOptionPane.showMessageDialog(null, "Modification avec succ√®s");

        // Reload the reservation table
        me.setReservations(action.getAllReservations());
        jTable2.setModel(me);
    }//GEN-LAST:event_ModActionPerformed

    private String formatDate(Date date) {
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        return dateFormat.format(date);
    }     
    
        private void updatePrix() {
    LocalDate dateDebut = Date_Debut.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
    LocalDate dateFin = Date_Fin.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

    if (dateDebut != null && dateFin != null) {
        long difference = Duration.between(dateDebut.atStartOfDay(), dateFin.atStartOfDay()).toHours();
        int duree = (int) difference;

        double PrixR = duree * 1.5;
        Prix.setText(Double.toString(PrixR));
        Duree.setText(Double.toString(duree));
    }}
    
