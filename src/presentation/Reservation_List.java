    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
    int ligne = jTable2.getSelectedRow();
    selectedid = Integer.parseInt(String.valueOf(me.getValueAt(ligne, 0)));
    String selectedDate_Debut = String.valueOf(me.getValueAt(ligne, 3));
    String selectedDate_Fin = String.valueOf(me.getValueAt(ligne, 4));
    String selectedDuree = String.valueOf(me.getValueAt(ligne, 5));
    String selectedPrix = String.valueOf(me.getValueAt(ligne, 6));

    // Parse the selected dates
    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
    try {
        Date dateDebut = dateFormat.parse(selectedDate_Debut);
        Date dateFin = dateFormat.parse(selectedDate_Fin);

        // Set the values in your UI components
        Date_Debut.setDate(dateDebut);
        Date_Fin.setDate(dateFin);
    } catch (ParseException e) {
        e.printStackTrace();
    }

    Duree.setText(selectedDuree);
    Prix.setText(selectedPrix);
    }//GEN-LAST:event_jTable2MouseClicked

